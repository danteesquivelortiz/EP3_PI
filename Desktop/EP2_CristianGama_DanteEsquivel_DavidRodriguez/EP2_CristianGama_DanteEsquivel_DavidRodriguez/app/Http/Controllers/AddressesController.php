    {
        //
        $address=new Addresses;
        $address->street=$request->street;
        $address->postcode=$request->postcode;
        $address->neighborhood=$request->neighborhood;
        $address->city=$request->city;
         if($address->save()){
            return redirect('/products');
        }else{
            return redirect('/addresses');
        }
    }

